a= 20
b= 10

# sum of two numbers 
sum = a+ b

print ("the sum of {0} and {1} is {2}".format(a, b, sum))

# sub of two numbers
sub = a-b

print ("the sub of {0} and {1} is {2}".format (a, b, sub))


#multiplication of tw numbers

mul= a*b

print ("the multiplication of {0} and {1} is {2}".format (a, b, mul))


#division of two numbers

div= a/b

print("division of {0} and {1} is {2}".format (a, b, div))

#modulus of two numbers

mod= a%b

print ('modulus of {0} and {1} is {2}'.format (a, b, mod))

#exponent of two numbers

exp= a**b

print ("the exponent of {0} and {1} is {2}".format (a, b, exp))


#floor division 

fd= a//b

print (" the floor dvision of {0} and {1} is {2}".format (a, b, fd))
